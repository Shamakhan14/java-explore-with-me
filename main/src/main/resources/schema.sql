CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    username VARCHAR(50) NOT NULL,
    email VARCHAR(50) NOT NULL,
    CONSTRAINT PK_USER PRIMARY KEY (id),
    UNIQUE (username)
);

CREATE TABLE IF NOT EXISTS categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    cat_name VARCHAR(50) NOT NULL,
    CONSTRAINT PK_CATEGORY PRIMARY KEY (id),
    UNIQUE (cat_name)
);

CREATE TABLE IF NOT EXISTS events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    annotation VARCHAR(2000) NOT NULL,
    category_id BIGINT NOT NULL,
    created_on TIMESTAMP NOT NULL,
    description VARCHAR(7000) NOT NULL,
    event_date TIMESTAMP NOT NULL,
    initiator BIGINT NOT NULL,
    location_lat FLOAT NOT NULL,
    location_lon FLOAT NOT NULL,
    paid BOOLEAN NOT NULL,
    participant_limit BIGINT NOT NULL,
    published_on TIMESTAMP,
    request_moderation BOOLEAN NOT NULL,
    state VARCHAR(10) NOT NULL,
    title VARCHAR(120) NOT NULL,
    CONSTRAINT PK_EVENT PRIMARY KEY (id),
    CONSTRAINT FK_EVENTS_TO_CATEGORIES FOREIGN KEY (category_id) REFERENCES categories(id),
    CONSTRAINT FK_EVENTS_TO_USERS FOREIGN KEY (initiator) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created TIMESTAMP NOT NULL,
    event_id BIGINT NOT NULL,
    requester BIGINT NOT NULL,
    status VARCHAR(10) NOT NULL,
    CONSTRAINT PK_REQUEST PRIMARY KEY (id),
    CONSTRAINT FK_REQUESTS_TO_EVENTS FOREIGN KEY (event_id) REFERENCES events(id),
    CONSTRAINT FK_REQUESTS_TO_USERS FOREIGN KEY (requester) REFERENCES users(id),
    UNIQUE (event_id, requester)
);

CREATE TABLE IF NOT EXISTS compilations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    pinned BOOLEAN NOT NULL,
    title VARCHAR(200) NOT NULL,
    CONSTRAINT PK_COMPILATION PRIMARY KEY (id),
    UNIQUE (title)
);

CREATE TABLE IF NOT EXISTS compilation_references (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    event_id BIGINT NOT NULL,
    compilation_id BIGINT NOT NULL,
    CONSTRAINT PK_REFERENCE PRIMARY KEY (id),
    CONSTRAINT FK_REFERENCES_TO_EVENTS FOREIGN KEY (event_id) REFERENCES events(id),
    CONSTRAINT FK_REFERENCES_TO_COMPILATIONS FOREIGN KEY (compilation_id) REFERENCES compilations(id)
);

CREATE TABLE IF NOT EXISTS comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    comment_text VARCHAR(500) NOT NULL,
    event_id BIGINT NOT NULL,
    creator BIGINT NOT NULL,
    created TIMESTAMP NOT NULL,
    CONSTRAINT PK_COMMENT PRIMARY KEY (id),
    CONSTRAINT FK_COMMENTS_TO_EVENTS FOREIGN KEY (event_id) REFERENCES events(id),
    CONSTRAINT FK_COMMENTS_TO_USERS FOREIGN KEY (creator) REFERENCES users(id)
);